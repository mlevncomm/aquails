function walkDir(dir, files = []) {
  if (!fs.existsSync(dir)) return files;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (entry.name === 'node_modules' || entry.name === 'ui') continue;
      walkDir(full, files);
    } else if (/\.(tsx?|jsx?)$/.test(entry.name) && !SKIP_FILES.has(entry.name)) {
      files.push(full);
    }
  }
  return files;
}

function fixContent(content) {
  let result = content;
  for (const [from, to] of REPLACEMENTS) {
    result = result.split(from).join(to);
  }
  return result;
}

let changedFiles = 0;
const files = TARGET_DIRS.flatMap((d) => walkDir(d));

for (const file of files) {
  const original = fs.readFileSync(file, 'utf8');
  const fixed = fixContent(original);
  if (fixed !== original) {
    fs.writeFileSync(file, fixed, 'utf8');
    changedFiles++;
    console.log('Updated:', path.relative(ROOT, file));
  }
}

console.log(`\nDone. ${changedFiles} file(s) updated.`);
